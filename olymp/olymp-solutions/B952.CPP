int almost_lucky(int x)
{
	if (x==0) return 0;
	int len=0,p=1;
	for (int i=0;i<=9;i++)
		if (x/p>0) len++,p*=10;else break;
	if ((len&1)==1) return 0;
	int min1=9,min2=9,max1=0,max2=0,a,sum1=0,sum2=0;
	int dg[10]={};
	for (int i=1;i<=len;i++)
	{
		a=x%10;
		if (i<=len/2)
		{
			min2=min(min2,a);
			max2=max(max2,a);
			sum2+=a;
		}
		if (i>len/2)
		{
			min1=min(min1,a);
			dg[a]++;
			sum1+=a;
		}
		x/=10;
	}
	for (int i=9;i>=0;i--)
	{
		if (dg[i]>0)
		{
			if (dg[i]==1 && i==a) max1=max(max1,i-1);else max1=max(max1,i);
		}
	}
	if (sum1<sum2)
	{
		if (sum1+9-min1>=sum2) return 1;
		if (sum1>=sum2-max2) return 1;
	}
	if (sum1>sum2)
	{
		if (sum2+9-min2>=sum1) return 1;
		if (sum2>=sum1-max1) return 1;
	}
	return 0;
}

