#include<iostream>
using namespace std;
typedef long long LL;

bool witness(int a,int n)
{
	LL d=1;
	int b=n-1;
	for (int i=31;i>=0;i--)
	{
		LL x=d;
		d=(d*d)%n;
		if (d==1 && x!=1 && x!=n-1) return true;
		if (b&(1<<i)) d=(d*a)%n;
	}
	return (d!=1);
}

bool MillerRabin(int n,int tries=3)
{
	while (tries-->0)
		if (witness(rand()%(n-1)+1,n)) return false;
	return true;
}

bool prime(int n)
{
	if (n==2) return true;
	if ((n>7) && (n%2==0 || n%3==0 || n%5==0 || n%7==0)) return false; 
	if (n==1 || !MillerRabin(n)) return false;
	return true;
}

